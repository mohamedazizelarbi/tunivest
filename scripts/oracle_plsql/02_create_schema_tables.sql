GRANT SELECT, INSERT, UPDATE, DELETE ON investments TO tunivest_rw_role;
GRANT SELECT, INSERT, UPDATE, DELETE ON portfolio TO tunivest_rw_role;
GRANT SELECT, INSERT, UPDATE, DELETE ON recommendations TO tunivest_rw_role;
GRANT SELECT, INSERT, UPDATE, DELETE ON transactions TO tunivest_rw_role;
GRANT SELECT, INSERT, UPDATE, DELETE ON market_data TO tunivest_rw_role;
GRANT SELECT, INSERT, UPDATE, DELETE ON simulations TO tunivest_rw_role;


GRANT SELECT ON investments TO tunivest_ro_role;
GRANT SELECT ON portfolio TO tunivest_ro_role;
GRANT SELECT ON recommendations TO tunivest_ro_role;
GRANT SELECT ON transactions TO tunivest_ro_role;
GRANT SELECT ON market_data TO tunivest_ro_role;
GRANT SELECT ON simulations TO tunivest_ro_role;
